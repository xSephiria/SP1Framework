void mapData(char c[][60]) {

	for (int cols = 0; cols < 25; cols++)
	{
		for (int rows = 0; rows < 60; rows++)
		{
			if (c[cols][rows] == G_WALL)
			{
				c[cols][rows] = (char)178;
			}
			if (c[cols][rows] == G_BOX)
			{
				c[cols][rows] = (char)177;
			}
			map[cols][rows] = c[cols][rows];
		}
	}
}

